static uint8_t *decode_embedded_logo(size_t *out_size)
{
	uint8_t *out;
	size_t in_pos = 0;
	size_t out_pos = 0;

	out = malloc(EMBEDDED_LOGO_BMP_SIZE);
	if (!out) {
		fprintf(stderr, "allocate embedded logo buffer failed\n");
		return NULL;
	}

	while (out_pos < EMBEDDED_LOGO_BMP_SIZE) {
		uint16_t header;
		uint16_t count;
		int is_run;
		uint16_t i;

		if (in_pos + 2 > sizeof(embedded_logo_rle_data))
			goto fail;

		header = read_le16(embedded_logo_rle_data + in_pos);
		in_pos += 2;
		is_run = !!(header & 0x8000);
		count = header & 0x7fff;
		if (!count)
			goto fail;

		if (is_run) {
			const uint8_t *unit;

			if (in_pos + EMBEDDED_LOGO_RLE_UNIT_SIZE > sizeof(embedded_logo_rle_data))
				goto fail;

			unit = embedded_logo_rle_data + in_pos;
			in_pos += EMBEDDED_LOGO_RLE_UNIT_SIZE;

			for (i = 0; i < count && out_pos < EMBEDDED_LOGO_BMP_SIZE; i++) {
				size_t copy = EMBEDDED_LOGO_RLE_UNIT_SIZE;

				if (copy > EMBEDDED_LOGO_BMP_SIZE - out_pos)
					copy = EMBEDDED_LOGO_BMP_SIZE - out_pos;

				memcpy(out + out_pos, unit, copy);
				out_pos += copy;
			}
		} else {
			size_t bytes = (size_t)count * EMBEDDED_LOGO_RLE_UNIT_SIZE;

			if (in_pos + bytes > sizeof(embedded_logo_rle_data))
				goto fail;

			if (bytes > EMBEDDED_LOGO_BMP_SIZE - out_pos)
				bytes = EMBEDDED_LOGO_BMP_SIZE - out_pos;

			memcpy(out + out_pos, embedded_logo_rle_data + in_pos, bytes);
			out_pos += bytes;
			in_pos += (size_t)count * EMBEDDED_LOGO_RLE_UNIT_SIZE;
		}
	}

	*out_size = EMBEDDED_LOGO_BMP_SIZE;
	return out;

fail:
	fprintf(stderr, "decode embedded logo failed\n");
	free(out);
	return NULL;
}

